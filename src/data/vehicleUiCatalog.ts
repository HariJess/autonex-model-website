export const VEHICLE_UI_CATALOG: VehicleUiCatalogEntry[] = Object.entries(VEHICLE_UI_CATALOG_BY_MAKE)
  .map(([make, models]) => ({
    make: normalizeName(make),
    models: Array.from(new Set(models.map((model) => normalizeName(model)).filter(Boolean))).sort((a, b) =>
      a.localeCompare(b),
    ),
    modelBodyTypes: Object.fromEntries(
      Object.entries(BODY_TYPE_BY_MODEL[make] ?? {})
        .map(([model, bodyTypes]) => [
          normalizeName(model),
          Array.from(new Set(bodyTypes)).filter(Boolean),
        ])
        .filter((entry) => entry[0] && entry[1].length > 0),
    ),
  }))
  .sort((a, b) => a.make.localeCompare(b.make));
